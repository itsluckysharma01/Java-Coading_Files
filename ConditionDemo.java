class ConditionDemo 
{
   public static void main(String args[])
   {
          int value1 = 10;
          int value2 = 20;
          if((value1 == 10) && (value2 == 20)) 
          System.out.println("value1 is 10 AND value2 is 20");
          if((value1 == 10) || (value2 == 100)) 
         System.out.println("value1 is 10 OR value2 is 10");
          if((value1 == 10) | (value2 == 100)) 
         System.out.println("value1 is 10 NOT value2 is 10");
        }
        }
