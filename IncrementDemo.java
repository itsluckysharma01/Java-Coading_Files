class IncrementDemo 
 {
    public static void main(String args[])
     {
         	 int x = 10;
         	 int y = 20;
         	System.out.println("X = "+x);
        	 System.out.println("Y = "+y);
        	System.out.println("The ++X =   " + ++x);
       	 System.out.println("The ++ y =   "+ ++y);
       	 System.out.println("The X++=     "+ x++);
         	System.out.println("The Y ++ =   "+ y++);
         	System.out.println("The X  Is =   "+ x);
         	System.out.println("The Y  Is=   "+ y);

       }
  }
